
public class SumDifference {

    public static void main(String[] args) {
        int num1 = 456;
        int num2 = 123;

        int sum = num1 + num2;
        int difference = num1 - num2;

        System.out.printf("%d      %d%n", num1, num1);
        System.out.printf("+%d     -%d%n", num2, num2);
        System.out.println("----     ----");
        System.out.printf("%d      %d%n", sum, difference);
    }
}
